def is_plaintext(file_path):
    f = magic.Magic(mime=True)
    return f.from_file(file_path) == 'text/plain'

def is_csv(file_path):
    f = magic.Magic(mime=True)
    return f.from_file(file_path) in ['application/csv', 'text/csv', 'text/plain']

